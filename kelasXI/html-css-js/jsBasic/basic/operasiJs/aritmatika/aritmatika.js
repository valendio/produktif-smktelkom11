function aritmatika (a,b) {
    document.write("Hasil operasi dasar dengan nilai a = " +a, " | b = "+b)
    document.write("<br>"+"perkalian  : a*b " + "= ")
    document.write(a*b)
    document.write("<br>"+"perkalian  : a/b " + "= ")
    document.write(a/b)
    document.write("<br>"+"perkalian  : a+b " + "= ")
    document.write(a+b)
    document.write("<br>"+"perkalian  : a-b " + "= ")
    document.write(a-b)
    document.write("<br>"+"perkalian  : a%b " + "= ")
    document.write(a%b)
}